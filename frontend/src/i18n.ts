const LOCALES: Record<LocaleCode, Dict> = { en, fr, es, it, de, pt, ar };
const RTL_LOCALES: LocaleCode[] = ["ar"];
const SUPPORTED: LocaleCode[] = ["en", "fr", "es", "it", "de", "pt", "ar"];

export const LANGUAGE_OPTIONS: { code: LocaleCode; label: string; native: string }[] = [
  { code: "en", label: "English",    native: "English"    },
  { code: "fr", label: "French",     native: "Français"   },
  { code: "es", label: "Spanish",    native: "Español"    },
  { code: "it", label: "Italian",    native: "Italiano"   },
  { code: "de", label: "German",     native: "Deutsch"    },
  { code: "pt", label: "Portuguese", native: "Português"  },
  { code: "ar", label: "Arabic",     native: "العربية"    },
];

const KEY_LOCALE = "innfeel_locale_override";

// Cached override (loaded once at boot) — synchronous reads are needed by `t()`.
let currentOverride: LocaleCode | null = null;
let overrideLoaded = false;
const listeners = new Set<() => void>();

function notifyChange() {
  for (const l of listeners) {
    try { l(); } catch {}
  }
}

function deviceLocale(): LocaleCode {
  try {
    const raw = (Localization.getLocales?.()[0]?.languageCode || "en").toLowerCase();
    if ((SUPPORTED as string[]).includes(raw)) return raw as LocaleCode;
  } catch {}
  return "en";
}

export function currentLocale(): LocaleCode {
  return currentOverride || deviceLocale();
}

export async function loadLocaleOverride(): Promise<LocaleCode | null> {
  if (overrideLoaded) return currentOverride;
  try {
    const v = await getItem(KEY_LOCALE);
    if (v && (SUPPORTED as string[]).includes(v)) {
      currentOverride = v as LocaleCode;
    }
  } catch {}
  overrideLoaded = true;
  return currentOverride;
}

export async function setLocaleOverride(code: LocaleCode | null): Promise<void> {
  if (code && !(SUPPORTED as string[]).includes(code)) return;
  currentOverride = code;
  try { await setItem(KEY_LOCALE, code || ""); } catch {}
  notifyChange();
}

export function t(key: string, fallback?: string): string {
  const loc = currentLocale();
  const dict = LOCALES[loc] || en;
  return dict[key] || en[key] || fallback || key;
}

/** React hook — re-renders components when the locale changes. */
export function useI18n() {
  const [, setTick] = useState(0);
  useEffect(() => {
    const cb = () => setTick((n) => n + 1);
    listeners.add(cb);
    // Ensure override is loaded at mount (covers first render before boot task finishes).
    if (!overrideLoaded) {
      loadLocaleOverride().then(() => setTick((n) => n + 1));
    }
    return () => {
      listeners.delete(cb);
    };
  }, []);
  return useMemo(() => ({ t, locale: currentLocale() }), [currentLocale()]);
}

export function isRTL(): boolean {
  return (RTL_LOCALES as string[]).includes(currentLocale());
}
