const KEY_LOCALE = "innfeel_locale_override";

// Cached override (loaded once at boot) — synchronous reads are needed by `t()`.
let currentOverride: LocaleCode | null = null;
let overrideLoaded = false;
const listeners = new Set<() => void>();

function notifyChange() {
  for (const l of listeners) {
    try { l(); } catch {}
  }
}

function deviceLocale(): LocaleCode {
  try {
    const raw = (Localization.getLocales?.()[0]?.languageCode || "en").toLowerCase();
    if ((SUPPORTED as string[]).includes(raw)) return raw as LocaleCode;
  } catch {}
  return "en";
}

export function currentLocale(): LocaleCode {
  return currentOverride || deviceLocale();
}

export async function loadLocaleOverride(): Promise<LocaleCode | null> {
  if (overrideLoaded) return currentOverride;
  try {
    const v = await getItem(KEY_LOCALE);
    if (v && (SUPPORTED as string[]).includes(v)) {
      currentOverride = v as LocaleCode;
    }
  } catch {}
  overrideLoaded = true;
  return currentOverride;
}

export async function setLocaleOverride(code: LocaleCode | null): Promise<void> {
  if (code && !(SUPPORTED as string[]).includes(code)) return;
  currentOverride = code;
  try { await setItem(KEY_LOCALE, code || ""); } catch {}
  notifyChange();
}

